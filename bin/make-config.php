<?php
declare(strict_types=1);

/**
 * Generates api/config.php from environment variables (fed by GitHub Secrets in CI).
 * Output goes to STDOUT — the deploy workflow redirects it into deploy/api/config.php.
 *
 * Values are written with var_export(), which safely escapes quotes and special
 * characters in passwords, so no shell-quoting issues can corrupt the file.
 *
 *   php bin/make-config.php > deploy/api/config.php
 */

function env(string $key, ?string $default = null): ?string
{
    $v = getenv($key);
    return ($v === false || $v === '') ? $default : $v;
}

$config = [
    'env' => 'production',
    'app' => [
        'base_url' => rtrim(env('CFG_BASE_URL', 'https://localhost'), '/'),
        'name'     => env('CFG_APP_NAME', 'ViscosiGuide'),
    ],
    'db' => [
        'driver'      => 'mysql',
        'host'        => env('CFG_DB_HOST', 'localhost'),
        'port'        => (int) env('CFG_DB_PORT', '3306'),
        'name'        => env('CFG_DB_NAME', ''),
        'user'        => env('CFG_DB_USER', ''),
        'password'    => env('CFG_DB_PASSWORD', ''),
        'sqlite_path' => '',
    ],
    'mail' => [
        'from_email' => env('CFG_MAIL_FROM_EMAIL', 'noreply@localhost'),
        'from_name'  => env('CFG_MAIL_FROM_NAME', 'ViscosiGuide'),
        'smtp' => [
            'host'       => env('CFG_SMTP_HOST', ''),
            'port'       => (int) env('CFG_SMTP_PORT', '587'),
            'encryption' => env('CFG_SMTP_ENCRYPTION', 'tls'),
            'username'   => env('CFG_SMTP_USERNAME', ''),
            'password'   => env('CFG_SMTP_PASSWORD', ''),
        ],
    ],
    'security' => [
        'session_idle_timeout' => (int) env('CFG_SESSION_IDLE_TIMEOUT', (string) (12 * 60 * 60)),
    ],
];

echo "<?php\n// Generated by bin/make-config.php during deploy. Do not edit by hand.\nreturn "
    . var_export($config, true)
    . ";\n";
